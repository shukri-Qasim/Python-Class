# Static function to divide two numbers
def div_static():
    num1 = 10
    num2 = 2
    result = num1 / num2
    print(result)
div_static()

# Static function to find the exponent of two numbers
def expon_static():
    base = 3
    exponent = 4
    result = base ** exponent
    print(result)
expon_static()


